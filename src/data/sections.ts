export function createEmptyPassport(): import('../types/passport').PassportData {
  const sections: Record<string, Record<string, string | string[]>> = {}
  for (const section of SECTIONS) {
    sections[section.id] = {}
    for (const field of section.fields) {
      sections[section.id][field.id] = field.type === 'list' ? [] : ''
    }
  }
  return {
    childName: '',
    childAge: '',
    sections,
    updatedAt: new Date().toISOString(),
  }
}
